import { defineConfig, devices } from "@playwright/test";
import * as path from "path";
import * as fs from "fs";

/**
 * Load .env.test variables so the dev server starts with test-friendly
 * Supabase / Redis / Gemini dummy values. Real .env values are preserved
 * as fallbacks.
 */
function loadEnvTest() {
  const envTestPath = path.resolve(__dirname, ".env.test");
  if (!fs.existsSync(envTestPath)) return {};
  const content = fs.readFileSync(envTestPath, "utf-8");
  const vars: Record<string, string> = {};
  for (const line of content.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eqIdx = trimmed.indexOf("=");
    if (eqIdx === -1) continue;
    vars[trimmed.slice(0, eqIdx)] = trimmed.slice(eqIdx + 1);
  }
  return vars;
}

const testEnv = loadEnvTest();

export default defineConfig({
  testDir: "./e2e",
  fullyParallel: true,
  forbidOnly: !!process.env.CI,
  retries: process.env.CI ? 2 : 0,
  workers: process.env.CI ? 1 : undefined,
  reporter: "html",
  timeout: 30_000,

  use: {
    baseURL: "http://localhost:3000",
    trace: "on-first-retry",
    screenshot: "only-on-failure",
  },

  projects: [
    {
      name: "chromium",
      use: { ...devices["Desktop Chrome"] },
    },
    {
      name: "mobile-chrome",
      use: { ...devices["Pixel 5"] },
    },
  ],

  webServer: {
    command: "npm run dev",
    url: "http://localhost:3000",
    reuseExistingServer: !process.env.CI,
    timeout: 120_000,
    env: {
      ...testEnv,
    },
  },
});
